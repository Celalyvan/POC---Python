operandoA = 3
operandoB = 2

suma =operandoA + operandoB

# print('valor suma:', suma)
# esta es la foma recomendada si se imprimen variables
print(f'resultado suma: {suma}')#se llama interpolacion, es necesario anteponer la f(formato) al string

resta = operandoA - operandoB
print(f'resultado resta: {resta}')

multip= operandoA * operandoB
print(f'resultado multiplicacion: {multip}')


division = operandoA / operandoB
print(f'resultado division: {division}')
divisionInt = operandoA // operandoB
print(f'resultado division entera: {divisionInt}')

modulo = operandoA % operandoB
print(f'resultado modulo: {modulo}')

exponente = operandoA ** operandoB
print(f'resultado potencia {exponente}')

